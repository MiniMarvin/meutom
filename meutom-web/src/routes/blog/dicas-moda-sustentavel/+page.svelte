	<h2>Por que a moda sustentável é importante?</h2>
	<p>
		Antes de mergulharmos nas dicas práticas, é essencial entender o impacto da moda no meio
		ambiente. A produção de roupas envolve processos intensivos em recursos, como o cultivo de
		algodão, que consome grandes quantidades de água, e o uso de produtos químicos para tingimento e
		acabamento. Além disso, o descarte inadequado de roupas contribui para o aumento de resíduos em
		aterros sanitários.
